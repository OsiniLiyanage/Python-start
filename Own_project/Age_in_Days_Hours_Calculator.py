birth_year = int(input("Enter your birth year: "))
current_year = 2026

age_in_years = current_year - birth_year
age_in_days = age_in_years * 365
age_in_hours = age_in_days * 24

print(f"You are {age_in_years} years old.")
print(f"That's approximately {age_in_days} days.")
print(f"Or about {age_in_hours} hours!")







